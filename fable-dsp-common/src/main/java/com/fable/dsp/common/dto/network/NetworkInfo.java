package com.fable.dsp.common.dto.network;

import java.io.Serializable;


public class NetworkInfo implements Serializable {
    
    /**
     * 
     */
    private static final long serialVersionUID = -6559049152598809800L;

    private Long id;
    
    private String name;

    private String card_inner;
    
    private String card_outer;
    
    private String server_ip;
    
    private String mask;
    
    private String gateway;
    
    private String card_type;
    
    private String port_type;
    
    private Long servicePort;
    
    private Long serviceInPort;
    
    private Long serviceOutPort;
    
    private Long innerProxyPort;
    
    private Long outerProxyPort;

    
    public Long getId() {
        return id;
    }
    
    
    public void setId(Long id) {
        this.id = id;
    }

    
    
    public String getName() {
        return name;
    }


    
    public void setName(String name) {
        this.name = name;
    }


    public String getCard_inner() {
        return card_inner;
    }

    
    public void setCard_inner(String card_inner) {
        this.card_inner = card_inner;
    }

    
    public String getCard_outer() {
        return card_outer;
    }

    
    public void setCard_outer(String card_outer) {
        this.card_outer = card_outer;
    }

    
    public String getServer_ip() {
        return server_ip;
    }

    
    public void setServer_ip(String server_ip) {
        this.server_ip = server_ip;
    }

    
    public String getMask() {
        return mask;
    }

    
    public void setMask(String mask) {
        this.mask = mask;
    }

    
    public String getGateway() {
        return gateway;
    }

    
    public void setGateway(String gateway) {
        this.gateway = gateway;
    }

    
    public String getCard_type() {
        return card_type;
    }

    
    public void setCard_type(String card_type) {
        this.card_type = card_type;
    }


    
    public Long getServicePort() {
        return servicePort;
    }


    
    public void setServicePort(Long servicePort) {
        this.servicePort = servicePort;
    }


    
    public Long getInnerProxyPort() {
        return innerProxyPort;
    }


    
    public void setInnerProxyPort(Long innerProxyPort) {
        this.innerProxyPort = innerProxyPort;
    }


    
    public Long getOuterProxyPort() {
        return outerProxyPort;
    }


    
    public void setOuterProxyPort(Long outerProxyPort) {
        this.outerProxyPort = outerProxyPort;
    }


    
    public String getPort_type() {
        return port_type;
    }


    
    public void setPort_type(String port_type) {
        this.port_type = port_type;
    }


    
    public Long getServiceInPort() {
        return serviceInPort;
    }


    
    public void setServiceInPort(Long serviceInPort) {
        this.serviceInPort = serviceInPort;
    }


    
    public Long getServiceOutPort() {
        return serviceOutPort;
    }


    
    public void setServiceOutPort(Long serviceOutPort) {
        this.serviceOutPort = serviceOutPort;
    }
    
    
    
}
