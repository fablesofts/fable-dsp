public class FtpManagerException extends CommonWebException{

	private static final long serialVersionUID = -128914017612925983L;

	public FtpManagerException() {
		super();
	}

	public FtpManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public FtpManagerException(String message) {
		super(message);
	}

	public FtpManagerException(Throwable cause) {
		super(cause);
	}
	
}
