package com.fable.dsp.common.dto.dataswitch;

import java.io.Serializable;

/**
 * 详情页展示Dto
 * @author Administrator
 *
 */
public class PropertyDto implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 803939474088132010L;
	private String name;
	private String value;
	private String group;
	private String editor;
	
	
	
	
	

	public PropertyDto(String name, String value, String group, String editor) {
		this.name = name;
		this.value = value;
		this.group = group;
		this.editor = editor;
	}

	public PropertyDto() {
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	
	@Override
	public String toString() {
		// TODO Auto-generated method stub
		return String.format("name:%s-value:%s-group:%s-editor:%s\n",this.name,this.value,this.group);
	}
	
}
